#initialization for the value of sum and product
sum = 0
product = 0


#getting inputs for the 1st number and 2nd number value

first_number = int(input("Enter the value of the 1st number:"))
second_number = int(input("Enter the value of the 2nd number:"))


#setting formula for the computation of the sum and product

sum = first_number + second_number
product = first_number * second_number


#Displaying the computed value for sum and product

print("the sum of the two numbers is:", sum)
print("the product of two numbers is:", product)
